#!/bin/bash

echo "Удаление перечисленных вложений"

# Проверяем, передан ли аргумент с именем файла
if [ $# -ne 1 ]; then
    echo "Использование: $0 <файл_со_списком>"
    exit 1
fi

file_list="$1"

# Проверяем существование файла со списком
if [ ! -f "$file_list" ]; then
    echo "Ошибка: файл $file_list не существует!"
    exit 1
fi

# Читаем файл построчно и удаляем файлы
while IFS= read -r file; do
    # Пропускаем пустые строки
    [ -z "$file" ] && continue

    # Проверяем существование файла перед удалением
    if [ -e "$file" ]; then
        rm -v -- "$file"
    else
        echo "Внимание: файл '$file' не существует, пропускаем."
    fi
done < "$file_list"

echo "Готово."